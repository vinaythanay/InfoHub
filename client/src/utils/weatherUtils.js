// Weather utility functions

export const celsiusToFahrenheit = (celsius) => {
  return Math.round((celsius * 9/5) + 32)
}

export const fahrenheitToCelsius = (fahrenheit) => {
  return Math.round((fahrenheit - 32) * 5/9)
}

